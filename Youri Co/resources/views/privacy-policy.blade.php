    <section>
        <h2>How Youri Works</h2>

        <p>Every order in Youri follows a structured and verified workflow:</p>

        <ul>
            <li><strong>Ordered</strong> – Orders are created and appear instantly in the admin panel</li>
            <li><strong>Assigned</strong> – Orders are assigned to a staff member and secured with a unique QR code</li>
            <li><strong>Preparing</strong> – Staff scan the QR code to confirm physical handling of the order</li>
            <li><strong>Dispatching</strong> – QR verification confirms the order is out for delivery</li>
            <li><strong>Delivered</strong> – Final QR scan completes the order lifecycle</li>
        </ul>

        <p>
            Each status change requires QR code scanning, preventing false updates 
            and ensuring real-world verification at every step.
        </p>
    </section>

    <section>
